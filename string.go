// 指定した位置の要素を返す。indexが範囲外のときはdefaultValueを返す。
func GetOrDefaultString(slice []string, index int, defaultValue string) string {
	if index < len(slice) {
		return slice[index]
	}
	return defaultValue
}

// 要素を１つランダムに返す。
func SampleString(slice []string, r *rand.Rand) string {
	return slice[r.Intn(len(slice))]
}

// スライスの各要素を組み合わせたスライスを返す。
func CombineString(slice []string, slices ...[]string) [][]string {
	size := len(slice)
	for _, slice := range slices {
		size *= len(slice)
	}

	out := make([][]string, 0, size)
	for _, v := range slice {
		out = append(out, []string{v})
	}

	for _, slice := range slices {
		out = combineString(out, slice)
	}

	return out
}

func combineString(out [][]string, slice []string) [][]string {
	length := len(out)
	for i := 0; i < length; i++ {
		for j := 0; j < len(slice); j++ {
			out = append(out, append(out[i], slice[j]))
		}
	}
	return out[length:]
}

// １つでも値と一致する要素が存在したらtrue。
func ContainsString(slice []string, v string) bool {
	for i := range slice {
		if slice[i] == v {
			return true
		}
	}
	return false
}

// １つでも条件を満たす要素が存在したらtrue。
func ContainsStringFunc(slice []string, f func(string) bool) bool {
	for i := range slice {
		if f(slice[i]) {
			return true
		}
	}
	return false
}

// 他のスライスのすべての要素を内包していたらtrue。
func ContainsAllString(slice []string, subset []string) bool {
	for i := range subset {
		if !ContainsString(slice, subset[i]) {
			return false
		}
	}
	return true
}

